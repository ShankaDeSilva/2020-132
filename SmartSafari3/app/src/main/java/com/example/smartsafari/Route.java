package com.example.smartsafari;

public class Route {

    private float startLat;

    private float startLon;

    private float lat;

    private float lon;

    private float endLat;

    private float endLog;

    public float getStartLat() {
        return startLat;
    }

    public float getStartLon() {
        return startLon;
    }

    public float getLat() {
        return lat;
    }

    public float getLon() {
        return lon;
    }

    public float getEndLat() {
        return endLat;
    }

    public float getEndLog() {
        return endLog;
    }
}
